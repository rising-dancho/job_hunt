// 1. Arithmetic Operators
let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 5 % 5; // gives the remainder

// Concatenation
x = 'rising.' + 'dancho';

// Exponent
x = 2 ** 2;

// Increment
x = 1;
x = x + 1;
x = x++;

// Decrement
x = x - 1;
x = x--;

console.log(x);

// 2. Assignment Operators
x = 10;
x += 5;
x -= 5;
x *= 5;
x /= 5;
x **= 5;
x %= 5;
console.log(x);

// 3. Comparison Operators

x = 2 == '2';
x = 2 === '2';
x = 2 != '2';
x = 2 !== 2;

x = 10 > 5;
x = 10 < 5;
x = 10 >= 5;
x = 10 <= 5;

console.log(x);
